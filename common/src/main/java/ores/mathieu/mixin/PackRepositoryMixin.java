package ores.mathieu.mixin;

import net.minecraft.server.packs.repository.PackRepository;
import net.minecraft.server.packs.repository.RepositorySource;
import org.spongepowered.asm.mixin.Mixin;
import org.spongepowered.asm.mixin.Mutable;
import org.spongepowered.asm.mixin.Shadow;
import org.spongepowered.asm.mixin.injection.At;
import org.spongepowered.asm.mixin.injection.Inject;
import org.spongepowered.asm.mixin.injection.callback.CallbackInfo;

import java.util.LinkedHashSet;
import java.util.Set;

@Mixin(PackRepository.class)
public class PackRepositoryMixin {
    @Shadow
    @Mutable
    public Set<RepositorySource> sources;

    @Inject(method = "<init>*", at = @At("RETURN"))
    private void onInit(RepositorySource[] repositorySources, CallbackInfo ci) {
        this.sources = new LinkedHashSet<>(this.sources);
        this.sources.add(new ores.mathieu.resource.VirtualPackSource());
    }
}
